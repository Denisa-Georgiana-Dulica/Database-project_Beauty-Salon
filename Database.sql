ALTER TABLE ANGAJATI_SALON ADD CONSTRAINT ck_salariu CHECK(SALARIU > 3300);

CREATE TABLE CLIENTI_SALON(
ID_CLIENT_SALON NUMBER(3),
NUME_CLIENT_SALON VARCHAR2(30) NOT NULL,
PRENUME_CLIENT_SALON VARCHAR2(30) NOT NULL,
TELEFON_CLIENT_SALON VARCHAR2(30),
SEX_CLIENT VARCHAR2(30),
EMAIL_CLIENT_SALON VARCHAR2(30),
DATA_NASTERII_CLIENT DATE,
CONSTRAINT pkId_client PRIMARY KEY(ID_CLIENT_SALON),
CONSTRAINT ckEmail_client CHECK(EMAIL_CLIENT_SALON LIKE '%@%.%')
