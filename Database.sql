CREATE TABLE SERVICII_SALON(
ID_SERVICIU NUMBER(3),
DENUMIRE_SERVICIU VARCHAR2(20) NOT NULL,
PRET_SERVICIU NUMBER(5,2),
DURATA_SERVICIU_MINUTE NUMBER(4),
CONSTRAINT pk_Id_serviciu PRIMARY KEY (ID_SERVICIU)
);

CREATE TABLE PROGRAMARI(
ID_PROGRAMARE NUMBER(3),
ID_CLIENT_SALON NUMBER(3),
ID_ANGAJAT_SALON NUMBER(3),
DATA_PROGRAMARE DATE,
ID_SERVICIU NUMBER(3),
STARE_PROGRAMARE VARCHAR2(20),
CONSTRAINT pk_Id_programare PRIMARY KEY (ID_PROGRAMARE),
CONSTRAINT fk_id_client_salon FOREIGN KEY(ID_CLIENT_SALON) REFERENCES CLIENTI_SALON(ID_CLIENT_SALON) ON DELETE CASCADE,
CONSTRAINT fk_id_angajat_salon FOREIGN KEY(ID_ANGAJAT_SALON) REFERENCES ANGAJATI_SALON(ID_ANGAJAT_SALON),
CONSTRAINT fk_id_serviciu FOREIGN KEY(ID_SERVICIU) REFERENCES SERVICII_SALON(ID_SERVICIU),
CONSTRAINT ck_stare_prog CHECK(STARE_PROGRAMARE IN ('CONFIRMATA','ANULATA','FINALIZATA'))
);

CREATE TABLE ISTORIC_SERVICII(
ID_CLIENT_SALON NUMBER(3),
ID_SERVICIU NUMBER(3),
RATING VARCHAR2(10),
CONSTRAINT pk_istoric PRIMARY KEY(ID_CLIENT_SALON,ID_SERVICIU),
CONSTRAINT fk_id_client_salon_istoric FOREIGN KEY(ID_CLIENT_SALON) REFERENCES CLIENTI_SALON(ID_CLIENT_SALON) ON DELETE CASCADE,
CONSTRAINT fk_id_serviciu_istoric FOREIGN KEY(ID_SERVICIU) REFERENCES SERVICII_SALON(ID_SERVICIU) ON DELETE CASCADE
