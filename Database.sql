CREATE TABLE ANGAJATI_SALON(
ID_ANGAJAT_SALON NUMBER(3),
NUME_ANGAJAT VARCHAR2(30) NOT NULL,
PRENUME_ANGAJAT VARCHAR2(30) NOT NULL,
SPECIALIZARE VARCHAR2(30),
